# ১. ফাংশন ডেফিনিশন (এটি শুধু নিয়ম বা রেসিপি)
def generate_data_split_table_no_index(y_train, y_val, y_test):
    train_classes = pd.Series(y_train).value_counts().sort_index()
    val_classes = pd.Series(y_val).value_counts().sort_index()
    test_classes = pd.Series(y_test).value_counts().sort_index()

    class_map = {
        0: "Hate Speech",
        1: "Not offensive",
        2: "Offensive but not Hate Speech"
    }

    table = PrettyTable()
    table.title = "TABLE II. DETAILS OF DATA SPLIT"
    table.field_names = ["Class", "Total Instances", "Training Instances", "Validation Instances", "Testing Instances"]

    total_inst_sum = total_train_sum = total_val_sum = total_test_sum = 0

    for cls in sorted(class_map.keys()):
        tr_c = train_classes.get(cls, 0)
        vl_c = val_classes.get(cls, 0)
        ts_c = test_classes.get(cls, 0)
        total_c = tr_c + vl_c + ts_c

        table.add_row([class_map[cls], total_c, tr_c, vl_c, ts_c])

        total_inst_sum += total_c
        total_train_sum += tr_c
        total_val_sum += vl_c
        total_test_sum += ts_c

    table.add_row(["Total", total_inst_sum, total_train_sum, total_val_sum, total_test_sum], divider=True)
    print(table)

