def lemmatization_fast(texts):
    lemma_list = []
    # disable=['ner', 'parser'] দিলে কোড অনেক দ্রুত চলবে
    for doc in nlp.pipe(texts, disable=['ner', 'parser'], batch_size=1000):
        lemma_list.append(' '.join([word.lemma_ for word in doc]))
    return lemma_list

